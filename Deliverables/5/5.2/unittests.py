import unittest
from go import GoBoard, GoPlayerCapture

class TestFunctions(unittest.TestCase):
    # ============= TEST GoPlayerCapture Class ============
    def testRegister(self):
        gpc = GoPlayerCapture()
        self.assertEqual(gpc.register(), "no name")
        self.assertEqual(gpc.register(), gpc.player_name)

    def testReceive(self):
        gpc = GoPlayerCapture()
        gpc.receive_stone("W")
        self.assertEqual(gpc.player_stone, "W")

        gpc2 = GoPlayerCapture()
        gpc.receive_stone("B")
        self.assertEqual(gpc2.player_stone, "B")

    def testFindMove(self):
        gpc = GoPlayerCapture()
        self.assertEqual(gpc.find_move("B", gb1[0], gb1), "1-5")
        self.assertEqual(gpc.find_move("B", gb1[1], [gb1[1], gb1[2]]), "1-1")
        self.assertEqual(gpc.find_move("W", gb2[0], gb2), "1-2")

    def testCountAdjLiberties(self):
        gpc = GoPlayerCapture()
        self.assertEqual(gpc.count_adj_liberties("3-2", gb1[0]), 0)
        self.assertEqual(gpc.count_adj_liberties("3-1", gb1[0]), 2)
        self.assertEqual(gpc.count_adj_liberties("6-2", gb1[0]), 4)

    def testCountSameStone(self):
        gpc = GoPlayerCapture()
        self.assertEqual(gpc.count_same_stone("3-2", "B", gb1[0]), 1)
        self.assertEqual(gpc.count_same_stone("6-7", "B", gb1[0]), 3)
        self.assertEqual(gpc.count_same_stone("3-1", "W", gb1[0]), 0)
        self.assertEqual(gpc.count_same_stone("6-2", "W", gb1[0]), 0)





