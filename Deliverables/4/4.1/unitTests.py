    def testGetScore(self):
        gb = GoBoardComponent()
        self.assertEqual(gb.get_score(gb3), {"B":28, "W":19})
        self.assertEqual(gb.get_score(gb4), {"B":0, "W":361})
        self.assertEqual(gb.get_score(gb5), {"B":19, "W":323})
        self.assertEqual(gb.get_score(gb7), {"B":22, "W":2})
        self.assertEqual(gb.get_score(gb6), {"B":29, "W":17})
        self.assertEqual(gb.get_score(gb8), {"B": 0, "W": 0})

    def testGetResponse(self):
        gb = GoBoardComponent()
        self.assertEqual(gb.get_response(gb3), {"B":28, "W":19})
        self.assertTrue(gb.get_response(["B","pass"]))
        self.assertTrue(gb.get_response(["W","pass"]))
        self.assertTrue(gb.get_response(haha))
        self.assertTrue(gb.get_response(hehe))
        self.assertFalse(gb.get_response(hihi))
        self.assertFalse(gb.get_response(hoho))
        self.assertFalse(gb.get_response(huhu))
        self.assertTrue(gb.get_response(hiehie))
        self.assertTrue(gb.get_response(haehae))


