import unittest
from goboard import GoBoardComponent

class TestFunctions(unittest.TestCase):

#============= HELPER FUNCTION TESTS=================
    def testProcessPoints(self):
        gb = GoBoardComponent()
        self.assertEqual(gb.process_point("1-1"), (0,0))
        self.assertEqual(gb.process_point("19-15"), (14,18))
        self.assertEqual(gb.process_point("4-9"), (8,3))

    def testAddPoints(self):
        gb = GoBoardComponent()
        self.assertEqual(gb.get_str_point(0, 0), "1-1")
        self.assertEqual(gb.get_str_point(18, 4), "19-5")
        self.assertEqual(gb.get_str_point(2, 7), "3-8")

    def testFindNeighbors(self):
        gb = GoBoardComponent()
        # middle point
        self.assertEqual(sorted(gb.find_neighbors((1,2))), sorted([(1,1), (0,2), (2,2), (1,3)]))
        # corner points
        self.assertEqual(sorted(gb.find_neighbors((18,0))), sorted([(17,0), (18,1)]))
        self.assertEqual(sorted(gb.find_neighbors((0,0))), sorted([(1,0), (0,1)]))
        # point on edge
        self.assertEqual(sorted(gb.find_neighbors((5,0))), sorted([(4,0), (6,0), (5,1)]))

# ===================== TEST QUERIES ===================
    def testOccupied(self):
        gb = GoBoardComponent()
        self.assertTrue(gb.occupied((1,2), gb1))
        self.assertFalse(gb.occupied((0,0), gb1))

    def testOccupies(self):
        gb = GoBoardComponent()
        self.assertTrue(gb.occupies("W", (1,1), gb1))
        self.assertTrue(gb.occupies("B", (1,2), gb1))
        self.assertFalse(gb.occupies("W", (2,1), gb1))
        self.assertFalse(gb.occupies("B", (1,1), gb1))
        self.assertFalse(gb.occupies("W", (0,0), gb1))
        self.assertFalse(gb.occupies("B", (0,0), gb1))

    def testReachable(self):
        gb = GoBoardComponent()
        # point is the maybe stone
        self.assertTrue(gb.reachable((0,0), " ", gb1))
        self.assertTrue(gb.reachable((1,1), "W", gb1))
        self.assertTrue(gb.reachable((2,1), "B", gb1))

        # can reach through connected paths
        self.assertTrue(gb.reachable((5,0), "B", gb1))
        self.assertTrue(gb.reachable((5,0), " ", gb1))
        self.assertTrue(gb.reachable((4,9), "W", gb1))
        self.assertTrue(gb.reachable((4,9), " ", gb1))
        self.assertTrue(gb.reachable((4,0), "B", gb1))
        self.assertTrue(gb.reachable((4,0), "W", gb1))

        # cannot reach through connected paths
        self.assertFalse(gb.reachable((10,9), "W", gb1))
        self.assertFalse(gb.reachable((18,0), "W", gb1))
        self.assertFalse(gb.reachable((1,6), " ", gb1))

# ===================== TEST COMMANDS ===================
    def testPlace(self):
        gb = GoBoardComponent()
        # try to place where there is already something
        self.assertEqual(gb.place("W", (0,2), gb1), "This seat is taken!")

        # place in empty spaces and check
        a = gb.place("W", (0,3), gb1)
        self.assertEqual(a[0][3], "W")
        b = gb.place("B", (0,0), gb1)
        self.assertEqual(b[0][0], "B")

    def testRemove(self):
        gb = GoBoardComponent()
        # try to remove something that is not there
        self.assertEqual(gb.remove("W", (0,0), gb1), "I am just a board! I cannot remove what is not there!")
        self.assertEqual(gb.remove("B", (0,2), gb1), "I am just a board! I cannot remove what is not there!")

        # remove stones already on there
        c = gb.remove("W", (0,2), gb1)
        self.assertEqual(c[0][2], " ")
        d = gb.remove("B", (2,1), gb1)
        self.assertEqual(d[2][1], " ")

    def testGetPoints(self):
        gb = GoBoardComponent()

        self.assertEqual(gb.get_points("W", gb2), ["1-1", "1-10", "2-1", "2-5", "3-2", "4-1", "4-9"])
